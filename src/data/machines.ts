export function isConveyor(type: string): boolean {
  return type === 'conveyor' || type === 'fast_conveyor' || type === 'super_conveyor';
}

export function isProcessing(type: string): boolean {
  return ['smelter', 'advanced_smelter', 'cutter', 'chemical_plant',
    'assembler', 'advanced_assembler', 'advanced_factory', 'ship_assembly'
  ].includes(type);
}

export function isStorage(type: string): boolean {
  return ['small_storage', 'medium_storage', 'large_storage', 'trash'].includes(type);
}

export function isMiner(type: string): boolean {
  return ['miner_mk1', 'miner_mk2', 'miner_mk3', 'water_pump', 'oil_pump'].includes(type);
}

export function isGenerator(type: string): boolean {
  return ['coal_generator', 'bio_generator', 'solar_panel',
    'battery_station', 'nuclear_reactor'].includes(type);
}
